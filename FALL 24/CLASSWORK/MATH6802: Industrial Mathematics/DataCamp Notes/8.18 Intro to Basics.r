# 08/18 Intro to Basics

# Assigning Variables:
a <- 42 # Assigns the integer ('numeric') to a
a # Prints a

# Basic Data Types:
numeric <- 4.5
integer <- 4 # Integers are also considered 'numeric'.
boolean1 <- TRUE # Boolean values (TRUE and FALSE) are called 'logical'
boolean2 <- FALSE
characters <- "hello world" # Text, or strings, indicated by quotation marks, are called 'characters'.

# class() is a function that outputs the data type of an input:
class(42)

# Basic Operations:
x <- 1
y <- 2

x + y # Addition
x - y # Subtraction
x * y # Multiplication
x / y # Division
x ^ y # Exponentiation
x %% y # Modulus
x %/%y # Integer Division (Floor Function)
